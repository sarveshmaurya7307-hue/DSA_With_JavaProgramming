package BasicsOfjava;

public class incrementdecrement {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(x);
        x++;  // or x = x + 1; dono same hai
        System.out.println(x);
        x--;
        System.out.println(x);
        ++x;
        System.out.println(x);
        --x;
        System.out.println(x);
        System.out.println(x++);
        System.out.println(x);
        // x++ is use x and then increment
        // ++x is increment x and then use
    }
}
