package basicsOfjava;

public class doubleDatatypejava {
    public static void main(String[] args) {
        double x = 5;
        double y = 2;
        double z = x / y;
       System.out.println(z); // output is 2.5
        double a = 5 / 2;
        System.out.println(a); // output is 2.0
        double b = 5 / 2.0;
        System.out.println(b); // output is 2.5
        double c = 5.0 / 2;
        System.out.println(c); // output is 2.5
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);




    }
}
